
public class Math {
	
	double add(double a, double b) {
		return (a + b);
	}
	
	double sub(double a, double b){
		return (a - b);		
	}
	
	double add(double a, double b, double c) {
		return (a + b + c);
	}
	
	double add(double a, double b, double c, double d) {
		return (a + b + c + d);
	}
	
	int add(int a, double b, double c, double d) {
		return (int) (a + b + c + d);
	}

}
